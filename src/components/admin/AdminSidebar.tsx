import Link from 'next/link';
import { useRouter } from 'next/router';
import { useState } from 'react';
import { useAuth } from '@/context/AuthContext';

const AdminSidebar = () => {
  const { logout } = useAuth();
  const router = useRouter();
  
  const [isOpen, setIsOpen] = useState(true);

  const handleLogout = async () => {
    try {
      await logout();
      router.push('/login');
    } catch (error) {
      console.error('Logout error:', error);
    }
  };

  const navigation = [
    { name: 'Dashboard', href: '/admin', icon: HomeIcon },
    { name: 'Subscribers', href: '/admin/subscribers', icon: UsersIcon },
    { name: 'Content', href: '/admin/content', icon: DocumentTextIcon },
    { name: 'Newsletters', href: '/admin/newsletters', icon: MailIcon },
    { name: 'Settings', href: '/admin/settings', icon: CogIcon },
  ];

  function classNames(...classes: string[]) {
    return classes.filter(Boolean).join(' ');
  }

  return (
    <div className={`${isOpen ? 'w-64' : 'w-20'} transition-all duration-300 bg-primary-900 h-screen sticky top-0`}>
      <div className="flex h-full flex-col">
        <div className="flex items-center justify-between p-4 border-b border-primary-700">
          <div className={`flex items-center ${!isOpen && 'justify-center w-full'}`}>
            <div className="text-white font-bold text-xl">{isOpen ? 'GeoHog Admin' : 'GH'}</div>
          </div>
          <button 
            className="text-white hover:text-gray-300"
            onClick={() => setIsOpen(!isOpen)}
          >
            {isOpen ? (
              <ChevronLeftIcon className="h-5 w-5" />
            ) : (
              <ChevronRightIcon className="h-5 w-5" />
            )}
          </button>
        </div>

        <nav className="flex-1 space-y-1 px-2 py-4">
          {navigation.map((item) => {
            const isActive = router.pathname === item.href || router.pathname.startsWith(`${item.href}/`);
            return (
              <Link
                key={item.name}
                href={item.href}
                className={classNames(
                  isActive
                    ? 'bg-primary-800 text-white'
                    : 'text-gray-300 hover:bg-primary-700 hover:text-white',
                  'group flex items-center px-2 py-2 text-base font-medium rounded-md'
                )}
              >
                <item.icon
                  className={classNames(
                    isActive ? 'text-white' : 'text-gray-400 group-hover:text-white',
                    'mr-3 flex-shrink-0 h-6 w-6'
                  )}
                />
                {isOpen && item.name}
              </Link>
            );
          })}
        </nav>

        <div className="border-t border-primary-700 p-4">
          <button
            onClick={handleLogout}
            className="w-full flex items-center px-2 py-2 text-base font-medium rounded-md text-gray-300 hover:bg-primary-700 hover:text-white"
          >
            <LogoutIcon className="mr-3 flex-shrink-0 h-6 w-6 text-gray-400" />
            {isOpen && 'Logout'}
          </button>
        </div>
      </div>
    </div>
  );
};

